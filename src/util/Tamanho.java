package util;

public class Tamanho {

	// largura
	private int width;
	// altura
	private int height;

	public Tamanho(int width, int height) {
		this.width = width;
		this.height = height;
	}

	public Tamanho() {
	}

	public int getWidth() {
		return width;
	}

	public void setWidth(int width) {
		this.width = width;
	}

	public int getHeight() {
		return height;
	}

	public void setHeight(int height) {
		this.height = height;
	}

}
